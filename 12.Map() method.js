//The map() method in JavaScript is used to create a new array by applying a given function to each element of an existing array. It does not modify the original array.

//Example: Double the Elements of the array
const numbers = [1, 2, 3, 4];
const doubled = numbers.map((num) => num * 2);
console.log(doubled); // Output: [2, 4, 6, 8]
